package String;

public class String_Functions {

	public static void main(String[] args) {
		// Method-1// toLowercase
		String s = "Rajan";
		String y = s.toLowerCase();
		System.out.println(y);
		// Method-2// toUppercase
		String x = s.toUpperCase();
		System.out.println(x);
		// Method-3// length
		int a = s.length();
		System.out.println(a);
		// Method-4// charAt
		char aa = s.charAt(2);
		System.out.println(aa);
		// Method-5// Substring with 1 parameter
		String b = s.substring(2);
		System.out.println(b);
		// Method-6// substring with 2 parameter
		String bb = s.substring(1, 5);
		System.out.println(bb);

	}

}
